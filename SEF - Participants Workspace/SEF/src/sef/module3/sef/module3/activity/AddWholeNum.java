public class AddWholeNum {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int total = 0;
		for (int i = 1; i <= 50; i++) {
			total = total + i;
		}
		System.out.println("Addition of Whole Numbers from 1 to 50 = " + total);

		total = 0;

		// complete the code. write for loop for i
		int n = 50;
		for (int i = 1; i <= n; ++i) {
			System.out.println(i);
		}



	}

}
